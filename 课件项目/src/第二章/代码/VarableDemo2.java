package 第二章;

public class VarableDemo2 {

	public static void main(String[] args) {
		//分数
		double score = 98.5;
		//姓名
		String name = "张三";
		//性别
		char sex = '男';
		System.out.println("本次Java考试最高分："+score);
		System.out.println("最高分的学生姓名："+name);
		System.out.println("最高分学生性别："+sex);
	}

}
